    public static string NormalizeLanguage(string? language)
    {
        return string.Equals(language, "zh-CN", StringComparison.OrdinalIgnoreCase)
            ? "zh-CN"
            : "en-US";
    }

    public static string Text(string language, string key)
    {
        language = NormalizeLanguage(language);
        if (Resources.TryGetValue(language, out var localized) && localized.TryGetValue(key, out var value))
        {
            return value;
        }

        return Resources["en-US"].TryGetValue(key, out var fallback) ? fallback : key;
    }

    public static string Format(string language, string key, params object[] args)
    {
        return string.Format(CultureInfo.GetCultureInfo(NormalizeLanguage(language)), Text(language, key), args);
    }
